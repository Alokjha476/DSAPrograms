package Alpha4Batch.BitwiseOpt;

public class Operators {
    public static void main(String[] args) {
        System.out.println(4 & 5);   // AND  4
        System.out.println(3 | 5);  // OR   7
        System.out.println(3 ^ 5);  //XOR   6
        System.out.println(~5);    // One's  complement -6
        System.out.println(5 << 2); // left shift 20
        System.out.println(6 >> 1); // Right shift 1
    }
}
